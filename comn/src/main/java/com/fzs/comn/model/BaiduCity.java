package com.fzs.comn.model;

public class BaiduCity {

  public String name;
  public String pinyi;
  public int citycode;
  public int cityId;

  public BaiduCity(String name, String pinyi, int citycode, int cityId) {
    super();
    this.name = name;
    this.pinyi = pinyi;
    this.citycode = citycode;
    this.cityId = cityId;
  }
}
